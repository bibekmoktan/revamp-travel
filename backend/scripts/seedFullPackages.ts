// ── Seed runner ────────────────────────────────────────────────────────────

async function seed() {
  try {
    console.log('Connecting to MongoDB…');
    await mongoose.connect(MONGO_URL);
    console.log('Connected.\n');

    // Categories — upsert
    console.log('Seeding categories…');
    let catCreated = 0, catUpdated = 0;
    for (const c of categories) {
      const res = await CategoryModel.updateOne(
        { slug: c.slug },
        { $set: c },
        { upsert: true },
      );
      if (res.upsertedCount) catCreated++;
      else if (res.modifiedCount) catUpdated++;
    }
    console.log(`✓ Categories: ${catCreated} created, ${catUpdated} updated.\n`);

    // Packages — upsert
    console.log('Seeding packages…');
    let pkgCreated = 0, pkgUpdated = 0;
    for (const p of packages) {
      const res = await PackageModel.updateOne(
        { slug: p.slug },
        { $set: p },
        { upsert: true },
      );
      if (res.upsertedCount) pkgCreated++;
      else if (res.modifiedCount) pkgUpdated++;
    }
    console.log(`✓ Packages: ${pkgCreated} created, ${pkgUpdated} updated.`);
    console.log(`✓ Total packages in this seed: ${packages.length}\n`);

    await mongoose.disconnect();
    console.log('Done. Connection closed.');
    process.exit(0);
  } catch (err) {
    console.error('Seed failed:', err);
    process.exit(1);
  }
}

