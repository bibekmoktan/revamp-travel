// ── Inline lightweight schemas (no circular imports) ───────────────────────

const imageSchema = new mongoose.Schema(
  { url: String, alt: String },
  { _id: false },
);

const faqSchema = new mongoose.Schema(
  { question: String, answer: String },
  { _id: false },
);

const moreInfoSchema = new mongoose.Schema(
  { title: String, points: [String] },
  { _id: false },
);

const itinerarySchema = new mongoose.Schema(
  {
    day:         Number,
    title:       String,
    description: String,
    activities:  [String],
  },
  { _id: false },
);

const PackageModel =
  mongoose.models.Package ??
  mongoose.model(
    'Package',
    new mongoose.Schema(
      {
        slug:          { type: String, unique: true },
        title:         String,
        category:      String,
        country:       String,
        rating:        { type: Number, default: 0 },
        reviews:       { type: Number, default: 0 },
        duration:      String,
        price:         Number,
        featureImage:  imageSchema,
        gallery:       [imageSchema],
        description:   String,
        highlights:    [String],
        includes:      [String],
        notIncluded:   [String],
        location:      String,
        difficulty:    String,
        altitude:      String,
        groupSize:     String,
        bestSeason:    [String],
        tripStart:     String,
        tripEnd:       String,
        meals:         String,
        accommodation: String,
        mapUrl:        String,
        faq:           [faqSchema],
        moreInfo:      [moreInfoSchema],
        itinerary:     [itinerarySchema],
        status:        { type: String, default: 'active' },
      },
      { timestamps: true },
    ),
  );

const CategoryModel =
  mongoose.models.Category ??
  mongoose.model(
    'Category',
    new mongoose.Schema(
      {
        name:        String,
        slug:        { type: String, unique: true },
        description: String,
        image:       String,
        order:       Number,
        isActive:    { type: Boolean, default: true },
      },
      { timestamps: true },
    ),
  );

